namespace Pie;

public enum PixelFormat
{
    /// <summary>
    /// Unsigned byte RGBA format, with 8 bits for red, green, and blue, as well as 8 bits for alpha.
    /// </summary>
    R8G8B8A8_UNorm,

    /// <summary>
    /// Unsigned byte BGRA format, with 8 bits for blue, green, and red, as well as 8 bits for alpha.
    /// </summary>
    B8G8R8A8_UNorm
}