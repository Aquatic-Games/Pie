namespace Pie;

/// <summary>
/// Defines how polygons should be filled and drawn by the graphics device.
/// </summary>
public enum FillMode
{
    /// <summary>
    /// Polygons will be completely filled.
    /// </summary>
    Solid,
    
    /// <summary>
    /// Polygons will be drawn in wireframe.
    /// </summary>
    Wireframe
}