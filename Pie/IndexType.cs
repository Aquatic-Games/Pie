namespace Pie;

/// <summary>
/// The data type of the indices used when drawing.
/// </summary>
public enum IndexType
{
    /// <summary>
    /// These indices are an unsigned 16-bit integer.
    /// </summary>
    UShort,
    
    /// <summary>
    /// These indices are an unsigned 32-bit integer.
    /// </summary>
    UInt
}