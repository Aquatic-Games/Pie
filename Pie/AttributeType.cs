namespace Pie;

public enum AttributeType
{
    /// <summary>
    /// This attribute is an integer.
    /// </summary>
    Int = 1,
    
    /// <summary>
    /// This attribute is a two-component integer.
    /// </summary>
    Int2,
    
    /// <summary>
    /// This attribute is a three-component integer.
    /// </summary>
    Int3,
    
    /// <summary>
    /// This attribute is a four-component integer.
    /// </summary>
    Int4,
    
    /// <summary>
    /// This attribute is a float.
    /// </summary>
    Float,

    /// <summary>
    /// This attribute is a two-component float.
    /// </summary>
    Float2,
    
    /// <summary>
    /// This attribute is a three-component float.
    /// </summary>
    Float3,
    
    /// <summary>
    /// This attribute is a four-component float.
    /// </summary>
    Float4,
    
    /// <summary>
    /// This attribute is a byte.
    /// </summary>
    Byte,
    
    /// <summary>
    /// This attribute is a two-component byte.
    /// </summary>
    Byte2,

    /// <summary>
    /// This attribute is a four-component byte.
    /// </summary>
    Byte4,
    
    /// <summary>
    /// This attribute is a normalized byte.
    /// </summary>
    NByte,
    
    /// <summary>
    /// This attribute is a normalized two-component byte.
    /// </summary>
    NByte2,
    
    /// <summary>
    /// This attribute is a normalized four-component byte.
    /// </summary>
    NByte4
}